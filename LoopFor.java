package teste3;

/**
 *A instrução ++i, incrementa a variável iantes dela ser utilizada no comando. 
 * Já a instrução i++, incrementa a variável idepois dela ser utilizada no comando.
 * E se você incrementar uma variável e atribuir o resultado a ela mesma, em um comando,
 * o resultado da instrução é o valor da variável após o incremento.
 * @author Davi
 */
public class LoopFor {
    
    public static void main(String[]args){
    
        for(int i =0 ; i<=5; i++)
            System.out.println(i);
    }

    
}
